namespace Medja.Input
{
    /// <summary>
    /// Values for special keys
    /// </summary>
    public enum Keys
    {
        Backspace = '\b',
        Return = '\n',
        Tab = '\t',
        
        // like in javascript (browsers)
        Left = 37,
        Up = 38,
        Right = 39,
        Down = 40,
        
        // unicode delete char
        Delete = 127
    }
}